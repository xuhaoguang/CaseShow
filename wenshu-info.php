                        </div>
                    </div>

                    <div>
                        <hr class="wenshu-hr">
                    </div>

                    <div class="wenshu-title-2">
                        安徽省宿州市埇桥区人民法院<br>
                        民 事 判 决 书
                    </div>

                    <div class="wenshu-number">
                        （2015）宿埇民一初字第07734号
                    </div>

                    <div class="wenshu-content">
                        原告：史某，女。
                        <br>
                        委托代理人：许礼想。
                        <br>
                        被告：谷某甲，男。
                        <br><br>
                        原告史某诉被告谷某甲离婚纠纷一案，本院于2015年9月7日立案受理，依法由审判员马军适用简易程序，于2015年9月22日公开开庭进行了审理。原告史某及其委托代理人许礼想、被告谷某甲均到庭参加诉讼。本案现已审理终结。
                        <br><br>
                        原告史某诉称：原、被告于2003年经他人介绍相识并确立恋爱关系，年X月X日生育一子，取名谷某乙。于X年X月X日办理结婚登记。在原、被告相处的过程中，原告发现被告自私、暴躁，处事易走极端。2014年12月26日开始，原告通过被告的手机才发现被告与异性有暧昧关系且已有数日，原告当即劝阻，而被告拒不悔改。被告更以短信、微信、电话、QQ等保持暧昧关系，甚至双方在城区同居。原告数次为了挽救家庭，在劝阻无果后，在家中、义乌或者公共场所遭受被告的拳打脚踢和捆绑。被告数次怂恿异性恐吓威胁原告，让原告与被告离婚。为了彻底制服原告，在今年7月22日，被告又纠集其弟弟共同殴打原告，致使原告全身多处肿痛，面部、背部、双下肢、右上肢受伤基于上述事实，被告不仅违背夫妻之间的忠实义务，又与异性同居，对原告实施家庭暴力，致使夫妻间的感情破裂，已无和好可能，故请求判令原、被告离婚；依法分割夫妻共同财产；判令婚生子由被告抚养。
                        <br><br>
                        被告谷某甲辩称：原告诉称我打她不属实，我没有打她，没有和他人暧昧，只是打电话聊天。我弟弟打她，我没有；因为原告在高速公路上原告打闹，我才绑的她。
                        <br><br>
                        原告在诉讼过程中向法庭提供的证据材料为：1.身份证复印件，证明原告的主体资格；2.结婚证，证明原、被告之间的夫妻关系；3.分娩记录、户口本，证明婚生子的情况及与原告系母子关系；4.照片十张、门诊病历，证明被告及其弟弟殴打原告受伤；5.录音一份，证明被告与第三者有暧昧关系并同居的事实；6.贷款申请表，证明在2013年10月25日贷款25万元购买重型货车一辆。
                        上述证据材料，被告质证后表示：证据1，2，3，均无异议；证据，4，有异议，原告受伤不是我打的；证据5，无异议，因原告打电话骂她（指第三者），他说的气话；证据6，买车是事实，是和我弟弟一起买的。
                        被告在诉讼过程中未向法庭提供证据材料。<br>
                        本院听取了诉、辩双方的举证、质证意见，对原告提供的证据材料的证明效力认定如下：证据1，来源于公安机关，证据2，来源于民政部门；证据3，来源于医疗机构及公安机关；证据6，被告无异议，上述证据被告均无异议，其来源合法，内容真实，与本案具有关联性，其证明效力予以认定；证据4，5，被告有异议，原告亦未提供相应的证据予以佐证，其证明效力不予认定。
                        本院认为，综合原、被告的举证、质证意见及对证据证明效力的认定，本案的争议焦点为：原、被告之间的夫妻感情是否确已破裂。
                        本院经审理查明：原、被告于2003年10月份经人介绍相识并恋爱，于2006年农历正月初六按照习俗举行结婚仪式后即同居生活；于X年X月X日生育一子，取名谷某乙，于X年X月X日办理了结婚登记。婚后夫妻感情尚可。2015年9月2日原告以被告自私、暴躁，处事易走极端。2014年12月26日开始，原告通过被告的手机才发现被告与异性有暧昧关系且已有数日，原告当即劝阻，而被告拒不悔改。被告更以短信、微信、电话、QQ等保持暧昧关系，甚至双方在城区同居。原告数次为了挽救家庭，在劝阻无果后，在家中、义乌或者公共场所遭受被告的拳打脚踢和捆绑。被告数次怂恿异性恐吓威胁原告，让原告与被告离婚。为了彻底制服原告，在今年7月22日，被告又纠集其弟弟共同殴打原告，致使原告全身多处肿痛，面部、背部、双下肢、右上肢受伤基于上述事实，被告不仅违背夫妻之间的忠实义务，又与异性同居，对原告实施家庭暴力，致使夫妻间的感情破裂等理由诉讼来院，要求离婚。
                        <br><br>
                        本院认为，原、被告经婚姻登记机关登记结婚，其婚姻关系合法有效。在共同生活期间，原、被告的夫妻感情尚可，生育一子。原告称被告自私、暴躁，处事易走极端。2014年12月26日开始，原告通过被告的手机才发现被告与异性有暧昧关系且已有数日，原告当即劝阻，而被告拒不悔改。被告更以短信、微信、电话、QQ等保持暧昧关系，甚至双方在城区同居。原告数次为了挽救家庭，在劝阻无果后，在家中、义乌或者公共场所遭受被告的拳打脚踢和捆绑。被告数次怂恿异性恐吓威胁原告，让原告与被告离婚。为了彻底制服原告，在今年7月22日，被告又纠集其弟弟共同殴打原告，致使原告全身多处肿痛，面部、背部、双下肢、右上肢受伤基于上述事实，被告不仅违背夫妻之间的忠实义务，又与异性同居，对原告实施家庭暴力，致使夫妻间的感情破裂等理由要求离婚。被告对原告的诉称理由均予以否认，原告亦当庭自认无相应的证据证明夫妻感情确已破裂等主张，没有证据或者证据不足以证明当事人的事实主张的，由负有举证责任的当事人承担不利后果。故原告的诉讼请求，证据不足，本院不予支持。根据《中华人民共和国民事诉讼法》第六十四条第一款、第一百四十二条，《最高人民法院关于民事诉讼证据的若干规定》第二条第二款之规定，
                        <br><br>
                        判决如下：<br>
                        不准原告史某与被告谷某甲离婚。<br>
                        案件受理费100元，由原告史某全部承担。<br>
                        如不服本判决，可在判决书送达之日起十五日内，向本院递交上诉状，并按对方当事人的人数提出副本，上诉于安徽省宿州市中级人民法院。
                        <br><br>
                        审判员　马　军
                        <br>
                        二〇一五年九月二十五日
